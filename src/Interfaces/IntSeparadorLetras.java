    public void setLabelValidacion(String mensaje) {
        this.lblVerificacion.setText(mensaje);
    }

    public void setLabelImagenMostrar(ImageIcon imgMostrar) {
        Icon icono = new ImageIcon(imgMostrar.getImage().getScaledInstance(this.lblImagen.getWidth(), this.lblImagen.getHeight(),
                Image.SCALE_DEFAULT));
        this.lblImagen.setIcon( icono );
    }

    
    public void setTextNombreCarpeta(String  nombreCarpeta) {
        this.txtNombreCarpeta.setText(nombreCarpeta);
    }

    public void setInfoImagenes(String dividirImagen) {
        this.lblInfo.setText(dividirImagen);
    }

    public void setImagenDetecciones(Image imagen) {
        this.agregarImagenLabel(imagen, this.lblImagenResultado);
    }
    
    
    public void agregarImagenLabel(Image imagen, JLabel label) {
        ImageIcon icon = new ImageIcon(imagen.getScaledInstance(label.getWidth(), label.getHeight(),
                Image.SCALE_SMOOTH));
        label.setIcon(icon);
    }
