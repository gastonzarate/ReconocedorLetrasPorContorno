    private void btnBuscarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarImagenActionPerformed
        //Se busca la imagen
        JFileChooser elegirArchivo = new JFileChooser();
         
        FileNameExtensionFilter filtroImagen = new FileNameExtensionFilter("JPG, PNG , BPM", "jpg", "png", "bmp");
        elegirArchivo.setFileFilter(filtroImagen);

        if (elegirArchivo.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            //Envia al controlador el archivo de la imagen
            this.ctrlSeparadorLetras.setImagen(elegirArchivo.getSelectedFile());
        }
        
    }//GEN-LAST:event_btnBuscarImagenActionPerformed

